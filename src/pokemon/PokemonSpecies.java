	public static PokemonSpecies randomEncounter(Random rng) {
		List<PokemonSpecies> list = new ArrayList<PokemonSpecies>();
		for(PokemonSpecies p : PokemonSpecies.values()) {
			if(p.catchRate==0) {
				continue;
			}
			list.add(p);
		}
		
		return list.get(rng.nextInt(list.size()));
	}
	
	public static int countSpecies() {
		int count = 0;
		for(PokemonSpecies p : PokemonSpecies.values()) {
			if(p.catchRate==0 || p.formId>1) {
				continue;
			}
			count++;
		}
		return count;
	}
